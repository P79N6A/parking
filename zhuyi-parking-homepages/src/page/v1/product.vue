<script>
export default {
  data() {
    return {
      asideOffsetLeft: 0,
      aside: [true, false, false]
    }
  },
  mounted() {
    window.addEventListener('scroll', this.watchScroll)
  },
  created() {
    this.asideOffsetLeft = (document.body.clientWidth - 1920) / 2 + 1480
  },
  methods: {
    watchScroll () {
      let top = document.body.scrollTop || document.documentElement.scrollTop
      if (top >= 8195) {
        this.aside = [false, false, true]
      } else if (top >= 300) {
        this.aside = [false, true, false]
      } else {
        this.aside = [true, false, false]
      }
    },
    turnToDom(domId) {
      let domArr = ['prodBanner', 'prodApp', 'prodProd']
      let idx = domArr.indexOf(domId)
      this.aside = [false, false, false]
      this.aside[idx] = true
      if (document.body.scrollTop) {
        document.body.scrollTop = idx === 0 ? 0 : idx === 1 ? 500 : 8495
      } else {
        document.documentElement.scrollTop = idx === 0 ? 0 : idx === 1 ? 500 : 8495
      }
    },
  }
}
</script>
